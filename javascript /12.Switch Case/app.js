// Switch Case
 
let sayi= prompt("lütfen 1 ile 5 arasında bir sayı giriniz");

switch(sayi){
    case "1":
    console.log("girilen sayı 1");
    break;

    case "2":
    console.log("girilen sayı 2");
    break;

    case "3":
    console.log("girilen sayı 3");
    break;

    case "4":
    console.log("girilen sayı 4");
    break;

    case "5":
    console.log("girilen sayı 5");
    break;

    default :
    console.log("girilen değer 1 ile 5 arasında bir değer olmalıdır.");
    break;
}